"""Generate per-GPU bash run streams for the temporary critique-only regen.

Layout: 4 streams, run pair-wise:
  qwen_strong_gpu0.sh / qwen_strong_gpu1.sh   (6 cells each, run in parallel)
  llama_strong_gpu0.sh / llama_strong_gpu1.sh (6 cells each, run in parallel)

Strong only (subtle hasn't been trained yet).
"""

import os
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[2]))
from experiments.main_em_experiment import config as cfg

OUT = Path(cfg.EXP_ROOT) / "run_streams_critique"
OUT.mkdir(parents=True, exist_ok=True)

_CONDA_LIB = os.path.dirname(cfg.PYTHON).replace("/bin", "/lib")
HEADER = f"""#!/usr/bin/env bash
# AUTO-GENERATED by experiments/main_em_experiment/run_streams_critique_gen.py — do not hand-edit.
set -euo pipefail
cd "$(dirname "$0")/../../.."   # repo root
export PYTHONPATH="$PWD"
export LD_LIBRARY_PATH="{_CONDA_LIB}:${{LD_LIBRARY_PATH:-}}"
echo "[stream] $(date)  starting $(basename "$0")"
"""

PY = cfg.PYTHON
VARIANT = "strong"


def _cmd(model_key, d, t, gpus):
    return " ".join([
        PY, "-m", "experiments.main_em_experiment.regen_critique_one",
        "--model_key", model_key,
        "--ft_domain", d, "--ft_task", t, "--variant", VARIANT,
        "--gpus", gpus,
    ])


def main():
    cells = [(d, t) for d in cfg.DOMAINS for t in cfg.TASKS]  # 12 cells
    for model_key, tag in [("qwen2.5-14b", "qwen"), ("llama3.1-8b", "llama")]:
        for gpu_id, half in [("0", cells[0::2]), ("1", cells[1::2])]:
            lines = [HEADER]
            lines.append(f'echo "=== {model_key}  {VARIANT}  GPU{gpu_id}  critique-regen ==="')
            for d, t in half:
                lines.append(_cmd(model_key, d, t, gpu_id))
            lines.append('echo "[stream] DONE."')
            p = OUT / f"{tag}_{VARIANT}_gpu{gpu_id}.sh"
            p.write_text("\n".join(lines) + "\n")
            p.chmod(0o755)
            print(f"wrote {p}")


if __name__ == "__main__":
    main()
